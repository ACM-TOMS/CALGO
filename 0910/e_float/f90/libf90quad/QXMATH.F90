! Real-valued quad precision mathematical functions.

SUBROUTINE QXFLOOR(DST, X)
  IMPLICIT NONE
  REAL(16) DST, X
  INTEGER(8) N
  N = FLOOR(X, 8)
  DST = QFLOAT(N)
  RETURN
END

SUBROUTINE QXCEIL(DST, X)
  IMPLICIT NONE
  REAL(16) DST, X
  INTEGER(8) N
  N = CEILING(X, 8)
  DST = QFLOAT(N)
  RETURN
END

SUBROUTINE QXCBRT(DST, X)
  IMPLICIT NONE
  REAL(16) DST, X
  DST = X**0.3333333333333333333333333333333333333333Q0
  RETURN
END

SUBROUTINE QXROOTN(DST, X, P)
  IMPLICIT NONE
  REAL(16) DST, X
  INTEGER(4) P
  INTEGER(8) INT8
  DST = X**(1.0Q0/QFLOAT(INT8(P)))
  RETURN
END

