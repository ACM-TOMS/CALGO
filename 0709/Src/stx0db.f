                      INTEGER           IX0,    N
                      DOUBLE PRECISION  X0(N), FACTOR, TMP(N), ARGS(*)
C!!!!                 REAL              X0(N), FACTOR, TMP(N), ARGS(*)
C## S T A T U S:
C               IGNORE LINES BEGINNING WITH  "C!!!!" .
C
C               SYSTEM  DEPENDENCE:                      NONE.
C               SINGLE/DOUBLE CONVERSION: NEEDED (SEE CONVRT).
C
C               THIS VERSION IS IN   D O U B L E   PRECISION.
C!!!!           THIS VERSION IS IN   S I N G L E   PRECISION.

C>RCS $HEADER: STX0.F,V 2.2 91/11/20 10:53:10 BUCKLEY EXP $
C>RCS $LOG:     STX0.F,V $
C>RCS REVISION 2.2  91/11/20  10:53:10  BUCKLEY
C>RCS FINAL SUBMISSION TO TOMS
C>RCS
C>RCS REVISION 2.1  90/07/31  11:55:20  BUCKLEY
C>RCS REMOVED UNUSED VAR.
C>RCS
