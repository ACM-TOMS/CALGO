C>RCS INITIAL REVISION
C>RCS
C## D E S C R I P T I O N:
C
C    COMPUTE STANDARD STARTING POINTS FOR TEST FUNCTIONS AND PUT INTO
C    X0.  THE STARTING POINT IS DEFINED ACCORDING TO THE CODE IX0.
C    SEE THE ROUTINE ZZVECT.
C
C      IF IX0 = 0, X0(I) ARE DEFINED ON ENTRY FOR I=1,...,N.
C                ( THIS IS EQUIVALENT TO SPECIFYING IX0 = N. )
C      IF IX0 > 0, A CYCLE IS DEFINED AND MUST BE EXPANDED.
C      IF IX0 < 0  ON ENTRY, THEN A SELECTED FORMULA IS USED TO
C                  COMPUTE VALUES FOR THE COMPONENTS OF X0.
C
C    IN ANY CASE, ONCE THE VALUES FOR X0 ARE ESTABLISHED, ALL
C    COMPONENTS OF X0 ARE THEN MULTIPLIED BY FACTOR (UNLESS OF
C    COURSE FACTOR = 1).
C
C## E N T R Y   P O I N T S: THE NATURAL ENTRY ZZSTX0.
C## S U B R O U T I N E S:   ZZVECT.
C## P A R A M E T E R S:
      DOUBLE PRECISION  ZERO,       ONE,       TWO,       THREE
C!!!! REAL              ZERO,       ONE,       TWO,       THREE
      PARAMETER (       ZERO = 0D0, ONE = 1D0, TWO = 2D0, THREE = 3D0)

      DOUBLE PRECISION  FOUR,       FIVE,      SIX,       SEVEN
C!!!! REAL              FOUR,       FIVE,      SIX,       SEVEN
      PARAMETER (       FOUR = 4D0, FIVE = 5D0,SIX = 6D0, SEVEN = 7D0)

      DOUBLE PRECISION  EIGHT,         NINE,          TEN
C!!!! REAL              EIGHT,         NINE,          TEN
      PARAMETER (       EIGHT = 8D0,   NINE = 9D0,    TEN = 10D0     )

C## L O C A L   D E C L:     NONE ARE DEFINED.
C## S A V E:                 NONE SELECTED.
C## E Q U I V A L E N C E S: NONE ARE DEFINED.
C## C O M M O N:             NONE IS DEFINED.
C## D A T A:                 NO DATA VALUES SET.

C## E X E C U T I O N
C## E X E C U T I O N

      CALL ZZVECT ( IX0, N, TMP, X0, ARGS )

C-----NOW APPLY FACTOR TO THE STARTING POINT.

      IF ( FACTOR .NE. ONE ) THEN

         CALL ZZSCAL ( N, FACTOR, X0, 1 )

      ENDIF
      GOTO 90000

C## E X I T
90000      RETURN

C## F O R M A T S:  NONE ARE DEFINED.
C##                 E N D         OF ZZSTX0.
                    END
