%   This mex function returns two dimensional array
%   which enumerates k-combinations of {0,1,...,n-1} with no repetition.
%   Each column vector represents a k-combination.
%
%   Usage:
%      comb = ENUMCOMBWITHNOREP(n,k);
%
%   Example:
%      comb = ENUMCOMBWITHNOREP(5,3)
%
%      comb =
%      
%           0     0     0     0     0     0     1     1     1     2
%           1     1     1     2     2     3     2     2     3     3
%           2     3     4     3     4     4     3     4     4     4

