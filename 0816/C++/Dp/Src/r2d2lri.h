// CLASS PROVIDED:
//   DoubleIntegral - a class for two-dimensional repeated integrals of
//   the form
//
//             b  h(x)
//            I  I     f(x,y) dy dx,
//             a  g(x)
//
//   including an evaluation algorithm referred to as r2d2lri.
//
//   The public functions and member constants are described below.  Refer to
//   the files README.txt and r2d2lri_sample.cpp for comments on, and examples
//   of, the use of the DoubleIntegral class.
//
// TYPE DEFINITIONS:
//   BOUNDARY_FUNCTION = double (*)(double)
//   INTEGRAND_FUNCTION = double (*)(double,double)
//
// CONSTRUCTORS:
//   DoubleIntegral(double req_rel_acc = DEFAULT_REL_TOL,
//                  double req_abs_acc = DEFAULT_ABS_TOL,
//                  unsigned max_evals = DEFAULT_MAX_POINTS)
//      Sets the relative and absolute accuracies to which the value of the
//      DoubleIntegral is to be determined to req_rel_acc and req_abs_acc,
//      respectively.  The maximum number of function evaluations to be allowed
//      in the calculation of the DoubleIntegral is set to max_evals.
//      Sufficient space is allocated for the algorithm's generation of cubature
//      approximations and error estimates.  Private member values associated
//      with the algorithm are initialized.
//
//   DoubleIntegral(double a, double b, BOUNDARY_FUNCTION g,
//                  BOUNDARY_FUNCTION h, INTEGRAND_FUNCTION f,
//                  double req_rel_acc = DEFAULT_REL_TOL,
//                  double req_abs_acc = DEFAULT_ABS_TOL,
//                  unsigned max_evals = DEFAULT_MAX_POINTS)
//      Sets the outer and inner limits of integration to [a,b] and [g(x),h(x)],
//      respectively, and the integrand function to f(x,y).  The relative and
//      absolute accuracies to which the value of the integral is to be
//      determined are set to req_rel_acc and req_abs_acc, respectively.  The
//      maximum number of function evaluations to be allowed in the calculation
//      of the DoubleIntegral is set to max_evals.  Sufficient space is
//      allocated for the algorithm's generation of cubature approximations and
//      error estimates.  Private member values associated with the algorithm
//      are initialized.
//
// COPY CONSTRUCTOR:
//   DoubleIntegral(const DoubleIntegral& I)
//      Sets the limits of integration, integrand function, requested tolerances
//      and limit on the number of function evaluations allowed in r2d2lri to
//      the corresponding values for I.  All other private member values are set
//      to the corresponding member values of I.
//
// DESTRUCTOR:
//   ~DoubleIntegral()
//      Returns space used for the cubature approximations and error estimates
//      to the heap.
//
// ASSIGNMENT OPERATOR:
//   void operator =(const DoubleIntegral& I)
//      Sets all of the private member values to the corresponding values for I.
//
// MODIFICATION MEMBER FUNCTIONS:
//   void set_outer_interval(double a, double b)
//      Sets the outer interval of integration to [a,b] and re-initializes
//      private data members associated with the algorithm.
//
//   void set_inner_interval(BOUNDARY_FUNCTION g, BOUNDARY_FUNCTION h)
//      Sets the inner interval of integration to [g(x),h(x)] and re-initializes
//      private data members associated with the algorithm.
//
//   void set_integrand(INTEGRAND_FUNCTION f)
//      Sets the integrand function to f(x,y) and re-intializes private data
//      members associated with the algorithm.
//
//   void set_rel_tol(double req_rel_acc)
//      Sets the requested relative error to req_rel_acc and re-intializes 
//      private data members associated with the algorithm.
//
//   void set_abs_tol(double req_abs_acc)
//      Sets the requested absolute error to req_abs_acc and re-intializes
//      private data members associated with the algorithm.
//
//   void set_max_evals(unsigned max_evals)
//      Sets the maximum number of function evaluations to be permitted before
//      the r2d2lri algorithm is terminated to max_evals and re-intializes
//      private data members associated with the algorithm.
//
//   void set_new_integral(double a, double b, BOUNDARY_FUNCTION g,
//                         BOUNDARY_FUNCTION h, INTEGRAND_FUNCTION f)
//      Sets the outer and inner limits of integration to [a,b] and [g(x),h(x)],
//      respectively, and the integrand function to f(x,y).  Data members used
//      in the evaluation of the DoubleIntegral are re-initialized.  All other
//      private member variables (including rel_tol, abs_tol and max_points)
//      remain unchanged.
//
// ACCESS FUNCTIONS:
//   void get_outer_interval(double& a, double& b) const
//      Returns the outer interval of integration [a,b].
//
//   void get_inner_interval(BOUNDARY_FUNCTION& g, BOUNDARY_FUNCTION& h)
//      Returns the inner interval of integration.
//
//   void get_integrand(INTEGRAND_FUNCTION& f)
//      Returns the integrand function.
//
//   double get_rel_tol() const
//      Returns the requested relative error.
//
//   double get_abs_tol() const
//      Returns the requested absolute error.
//
//   unsigned get_max_evals() const
//      Returns the maximum number of function values permitted in the r2d2lri
//      algorithm.
//
//   double value() const
//      Returns the estimate of the DoubleIntegral computed using the r2d2lri
//      algorithm.
//
//   double rel_err_est() const
//      Returns r2d2lri's estimate of the relative error in its approximation
//      to the DoubleIntegral.  It is assumed that the DoubleIntegral has been
//      already approximated using one of the evaluation functions.  If this is
//      not the case, then the value -1 is returned.
//
//   double abs_err_est() const
//      Returns r2d2lri's estimate of the absolute error in its approximation
//      to the DoubleIntegral.  It is assumed that the DoubleIntegral has been
//      already approximated using one of the evaluation functions.  If this is
//      not the case, then the value -1 is returned.
//
//   int evals() const
//      Returns the number of function evaluations used by the r2d2lri algorithm
//      in approximating the DoubleIntegral.
//
//   int error_flag() const
//      Returns the value of the code which indicates the reason for terminating
//      the r2d2lri algorithm.  (-1 => DoubleIntegral not yet evaluated; 0 =>
//      normal convergence; >0 => abnormal termination)
//
// INTEGRAL EVALUATION FUNCTIONS:
//   double evaluate()
//      Returns the value of the approximation to the DoubleIntegral computed
//      by the r2d2lri algorithm.  It is assumed that a, b, g(x), h(x), f(x,y),
//      the requested relative and absolute accuracies and maximum number of
//      function values have all been set.
//
//   double evaluate(double req_rel_acc)
//      Sets the relative accuracy to which the value of the integral is to be
//      determined to req_rel_acc and returns the approximation which is then
//      computed by the r2d2lri algorithm.  It is assumed that a, b, g(x), h(x),
//      f(x,y), the requested absolute accuracy and the maximum number of
//      function values have all been set.
//
//   double evaluate(double& rel_err_est, int& flag, int& evals)
//      Returns the value of the approximation to the DoubleIntegral computed
//      by the r2d2lri algorithm, as well as rel_err_est, an estimate of the
//      relative error in the approximation, flag, an indication of the success
//      (or otherwise) of the algorithm, and evals, the number of function
//      values used by r2d2lri in approximating the DoubleIntegral.
//
//   double evaluate(double a, double b, BOUNDARY_FUNCTION g,
//                   BOUNDARY_FUNCTION h, INTEGRAND_FUNCTION f,
//                   double& rel_err_est, int& flag,
//                   double req_rel_acc = DEFAULT_REL_TOL,
//                   double req_abs_acc = DEFAULT_ABS_TOL,
//                   unsigned max_evals = DEFAULT_MAX_POINTS)
//      Sets the outer and inner limits of integration to [a,b] and [g(x),h(x)],
//      respectively, and the integrand function to f(x,y).  The relative and
//      absolute accuracies to which the value of the integral is to be
//      determined are set to req_rel_acc and req_abs_acc, respectively.  The
//      maximum number of function values to be used by the algorithm is set to
//      max_evals.
//      Returns the value of the approximation to the DoubleIntegral computed
//      by the r2d2lri algorithm, as well as rel_err_est, an estimate of the
//      relative error in the approximation, and flag, an indication of the
//      success (or otherwise) of the algorithm.
//
// MEMBER CONSTANTS:
//   static const unsigned SEED = 89
//      SEED is the value of the Fibonacci number upon which the initial
//      lattice used by r2d2lri is based.
//
//   static const unsigned PREV_FIB_NUMBER = 55
//      PREV_FIB_NUMBER is the number which precedes SEED in the Fibonacci
//      sequence.
//
//   static const unsigned DEFAULT_MAX_POINTS = 22784
//      Default maximum number of function evaluations to be used in the
//      r2d2lri algorithm.
//
//   static const unsigned DEFAULT_C_SIZE = 18
//      Default size of the array of cubature approximations used by r2d2lri.
//
//   static const unsigned POINTS = 16*SEED
//      Maximum number of distinct coordinate values used in the displaced
//      lattices.
// 
//   static const double DEFAULT_REL_TOL  = 0.0
//      Default value for requested relative accuracy in the computed value
//      of the DoubleIntegral.
//
//   static const double DEFAULT_ABS_TOL  = 0.0
//      Default value for requested absolute accuracy in the computed value
//      of the DoubleIntegral.
//
//   static const double SAFETY = 5.0E-15
//      Minimum reported error estimate.
//
//   static const double NEG_LOG2 = -0.301
//      Used in the go_fer function (= -log10(2.0))
//
